package com.rljc.module;

import javax.persistence.Entity;
import javax.persistence.Table;


/**
 * 广告位管理
 * @author hzq
 */
@Entity
@Table(name="ad_position")
public class AdPosition extends BaseEntity {
	private static final long serialVersionUID = 1L;
	private String title;//广告位标题
	private String positionCode;//广告位编码
	private Integer width;//广告位宽度
	private Integer height;//广告位高度
	private Short status;//状态
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPositionCode() {
		return positionCode;
	}
	public void setPositionCode(String positionCode) {
		this.positionCode = positionCode;
	}
	public Integer getWidth() {
		return width;
	}
	public void setWidth(Integer width) {
		this.width = width;
	}
	public Integer getHeight() {
		return height;
	}
	public void setHeight(Integer height) {
		this.height = height;
	}
	public Short getStatus() {
		return status;
	}
	public void setStatus(Short status) {
		this.status = status;
	}
	
}
