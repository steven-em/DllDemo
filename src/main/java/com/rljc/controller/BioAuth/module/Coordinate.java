package com.rljc.controller.BioAuth.module;

public class Coordinate {
	
	private Double x;
	
	private Double y;
	
	public Coordinate(){
		//设置默认值为0
		this(0.00,0.00);
	}
	
	public Coordinate(Double x,Double y){
		this.x = x;
		this.y = y;
	}
	

	public Double getX() {
		
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}

}
